def rdnumpy(txtname):  # 从txt文件中读取矩阵，注意每行之后都要加个空格
    f = open(txtname)
    line = f.readlines()
    lines = len(line)  # 行
    columns = 0  # 列
    for i in line:
        le = i.strip('\n').split(' ')
        columns = len(le) - 1
    A = np.zeros((lines, columns), dtype=int)
    A_row = 0
    for lin in line:
        templist = lin.strip('\n').split(' ')
        A[A_row:] = templist[0:columns]
        A_row += 1
    return A


